#!/bin/sh

##########################################################################################################################################################
# TEST #
########
APP_NAME=/usr/local/AppCentral/Wireguard2/
APKG_TEMP_DIR=/tmp/

##########################################################################################################################################################
# Déclaration de variable #
###########################
IMAGE=linuxserver/wireguard
CONTENEUR=wireguard
RESTART=unless-stopped
PORT=51820

##########################################################################################################################################################
# Fermeture du Conteneur #
##########################
docker container rm -f $CONTENEUR;

##########################################################################################################################################################
# Création du Conteneur #
#########################
docker create -i -t \
--name=$CONTENEUR \
--restart $RESTART \
--net=bridge \
--hostname $CONTENEUR \
--cap-add=NET_ADMIN \
--cap-add=SYS_MODULE \
--env PUID=1000 \
--env PGID=100 \
--env TZ=Etc/PARIS \
--publish $PORT \
$IMAGE:latest

##########################################################################################################################################################
# Démarrage du Conteneur #
##########################
docker start $CONTENEUR;

##########################################################################################################################################################
# Code Retour en Fermeture #
############################
exit 0
