#!/bin/sh

##########################################################################################################################################################
# DÃ©claration de Variable #
###########################
IMAGE=jlesage/jdownloader-2
CONTENEUR=Jdownloader


##########################################################################################################################################################
echo "pre-uninstall"
echo $IMAGE
echo $CONTENEUR

##########################################################################################################################################################
if [ ! -z $CONTENEUR ]; then 
	docker kill $CONTENEUR
	sleep 2
	docker rm -f $CONTENEUR
fi

##########################################################################################################################################################
if [ ! -z $IMAGE ]; then 
	if [ "$APKG_PKG_STATUS" == "uninstall" ]; then
		sleep 1
		docker rmi -f $IMAGE
	fi
fi

##########################################################################################################################################################
# Code Retour en Fermeture #
############################
exit 0
