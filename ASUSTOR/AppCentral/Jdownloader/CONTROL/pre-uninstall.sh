#!/bin/sh

##########################################################################################################################################################
# DÃ©claration de Variable #
###########################
IMAGE=jlesage/jdownloader-2
CONTENEUR=Jdownloader
container=$(docker container ls -a | grep $CONTENEUR |awk '{print $1}')
im=$(docker images | grep $CONTENEUR | grep latest | awk '{print $3}')

##########################################################################################################################################################
echo "pre-uninstall"


##########################################################################################################################################################
if [ ! -z $CONTENEUR ]; then 
	docker kill $CONTENEUR
	sleep 2
	docker rm -f $CONTENEUR
fi

##########################################################################################################################################################
if [ ! -z $im ]; then 
	if [ "$APKG_PKG_STATUS" == "uninstall" ]; then
		docker rmi -f $CONTENEUR
	fi
fi


##########################################################################################################################################################
# Code Retour en Fermeture #
############################
exit 0
